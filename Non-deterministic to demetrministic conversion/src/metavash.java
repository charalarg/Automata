public class metavash {
	
	public int trexousaKatastash;
	public int epomenhKatastash;
	public String sumvolo;

	
	public metavash(int trexousaKatastash, int epomenhKatastash, String sumvolo) {
		this.trexousaKatastash = trexousaKatastash;
		this.epomenhKatastash = epomenhKatastash;
		this.sumvolo = sumvolo;
	}

	public int getTrexousaKatastash() {
		return trexousaKatastash;
	}


	public void setTrexousaKatastash(int trexousaKatastash) {
		this.trexousaKatastash = trexousaKatastash;
	}


	public int getEpomenhKatastash() {
		return epomenhKatastash;
	}


	public void setEpomenhKatastash(int epomenhKatastash) {
		this.epomenhKatastash = epomenhKatastash;
	}


	public String getSumvolo() {
		return sumvolo;
	}


	public void setSumvolo(String sumvolo) {
		this.sumvolo = sumvolo;
	}
	
	
	
	

}
